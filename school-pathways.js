function getMiddleIdsForPrimary(primaryId) {
  return PRIMARY_TO_MIDDLE[primaryId] || [];
}

function getHighIdsForMiddle(middleId) {
  return MIDDLE_TO_HIGH[middleId] || [];
}

function getPrimaryIdsForMiddle(middleId) {
  return Object.entries(PRIMARY_TO_MIDDLE)
    .filter(([, mids]) => mids.includes(middleId))
    .map(([pid]) => pid);
}

function getMiddleIdsForHigh(highId) {
  return Object.entries(MIDDLE_TO_HIGH)
    .filter(([, highs]) => highs.includes(highId))
    .map(([mid]) => mid);
}

function schoolById(id) {
  return typeof SCHOOLS !== 'undefined' ? SCHOOLS.find(s => s.id === id) : null;
}

function resolvePathSchool(id) {
  const s = schoolById(id);
  if (s) return s;
  return { id: null, name: id, _textOnly: true };
}
